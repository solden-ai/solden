def _quickbooks_headers(connection) -> Dict[str, str]:
    return {
        "Authorization": f"Bearer {connection.access_token}",
        "Content-Type": "application/json",
        "Accept": "application/json",
    }


def _extract_quickbooks_fault_message(payload: Optional[Dict[str, Any]]) -> Optional[str]:
    if not isinstance(payload, dict):
        return None
    fault = payload.get("Fault")
    if isinstance(fault, dict):
        errors = fault.get("Error")
        if isinstance(errors, list):
            for error in errors:
                if not isinstance(error, dict):
                    continue
                for key in ("Detail", "Message", "code"):
                    detail = str(error.get(key) or "").strip()
                    if detail:
                        return detail
    for key in ("fault", "error", "message"):
        detail = str(payload.get(key) or "").strip()
        if detail:
            return detail
    return None


# ==================== Journal Entry ====================

async def post_to_quickbooks(
    connection,
    entry: Dict[str, Any],
) -> Dict[str, Any]:
    """
    Post journal entry to QuickBooks Online.

    Uses QuickBooks API:
    https://developer.intuit.com/app/developer/qbo/docs/api/accounting/all-entities/journalentry
    """
    if not connection.access_token or not connection.realm_id:
        return {"status": "error", "erp": "quickbooks", "reason": "QuickBooks not properly configured"}

    # Build QuickBooks journal entry format
    qb_entry = {
        "TxnDate": entry.get("date", datetime.now(timezone.utc).strftime("%Y-%m-%d")),
        "PrivateNote": entry.get("description", "Auto-generated by Solden"),
        "Line": [],
    }

    for line in entry.get("lines", []):
        qb_line = {
            "DetailType": "JournalEntryLineDetail",
            "Amount": line.get("debit", 0) or line.get("credit", 0),
            "JournalEntryLineDetail": {
                "PostingType": "Debit" if line.get("debit", 0) > 0 else "Credit",
                "AccountRef": {
                    "value": line.get("account", "1"),
                    "name": line.get("account_name", "Unknown"),
                },
            },
        }
        qb_entry["Line"].append(qb_line)

    # Make API call
    url = f"https://quickbooks.api.intuit.com/v3/company/{connection.realm_id}/journalentry"

    try:
        client = get_http_client()
        response = await client.post(
            url,
            json=qb_entry,
            headers={
                "Authorization": f"Bearer {connection.access_token}",
                "Content-Type": "application/json",
                "Accept": "application/json",
            },
            timeout=30,
        )

        if response.status_code == 401:
            # Token expired - would need to refresh
            logger.warning("QuickBooks token expired, needs refresh")
            return {"status": "error", "erp": "quickbooks", "reason": "Token expired", "needs_reauth": True}

        response.raise_for_status()
        result = response.json()

        logger.info(f"Posted to QuickBooks: {result.get('JournalEntry', {}).get('Id')}")
        return {
            "status": "success",
            "erp": "quickbooks",
            "entry_id": result.get("JournalEntry", {}).get("Id"),
            "sync_token": result.get("JournalEntry", {}).get("SyncToken"),
        }

    except httpx.HTTPStatusError as e:
        logger.error("QuickBooks API error: %s", e.response.status_code)
        return {"status": "error", "erp": "quickbooks", "reason": f"QuickBooks API {e.response.status_code}"}
    except Exception as e:
        logger.error("QuickBooks error: %s", type(e).__name__)
        return {"status": "error", "erp": "quickbooks", "reason": "posting_failed"}


# ==================== Token Refresh ====================

async def refresh_quickbooks_token(connection) -> Optional[str]:
    """Refresh QuickBooks OAuth token."""
    if not connection.refresh_token or not connection.client_id or not connection.client_secret:
        return None

    try:
        client = get_http_client()
        response = await client.post(
            "https://oauth.platform.intuit.com/oauth2/v1/tokens/bearer",
            data={
                "grant_type": "refresh_token",
                "refresh_token": connection.refresh_token,
            },
            auth=(connection.client_id, connection.client_secret),
        )
        response.raise_for_status()
        tokens = response.json()

        connection.access_token = tokens.get("access_token")
        connection.refresh_token = tokens.get("refresh_token")

        return connection.access_token
    except Exception as e:
        logger.error("Failed to refresh QuickBooks token: %s", type(e).__name__)
        return None


# ==================== Bill Posting ====================

async def post_bill_to_quickbooks(
    connection,
    bill,
    gl_map: Optional[Dict[str, str]] = None,
    field_mappings: Optional[Dict[str, str]] = None,
    custom_fields: Optional[Dict[str, str]] = None,
    idempotency_key: Optional[str] = None,
) -> Dict[str, Any]:
    """
    Post vendor bill to QuickBooks Online.

    API: https://developer.intuit.com/app/developer/qbo/docs/api/accounting/all-entities/bill

    ``field_mappings`` (Module 5) lets the customer map line-level
    Class / Department / Location dimensions. ``custom_fields`` is
    the resolved {erp_field_id: value} dict — for QBO custom fields
    this becomes the ``CustomField`` array on the Bill body so they
    show up on customer-defined templates.

    ``idempotency_key`` is forwarded to Intuit as the ``requestid``
    query parameter — QBO uses it to dedupe duplicate POSTs on retry.
    Caller should pass a stable key derived from the AP item id (the
    contract enforced by ``InvoicePostingMixin._post_to_erp``). Max
    50 chars per Intuit's spec; longer keys are truncated.
    """
    from solden.integrations.erp_router import get_account_code

    if not connection.access_token or not connection.realm_id:
        return {"status": "error", "erp": "quickbooks", "reason": "QuickBooks not properly configured"}

    # Duplicate-post pre-check (mirrors NetSuite/SAP). Intuit's requestid
    # dedupe only spans posts that share the same key — a manual resume
    # after a timeout-that-actually-succeeded posts with a different key
    # (resume:<ap> vs auto:<ap>) and QB has no other native guard, so a
    # silent-success timeout could otherwise create a duplicate Bill on
    # resume. Look the bill up by invoice number first and short-circuit
    # with already_posted if it's there. Failure of this lookup is
    # non-fatal; we proceed and rely on the router H10 audit-key check.
    if idempotency_key and getattr(bill, "invoice_number", None):
        try:
            existing_bill = await find_bill_quickbooks(
                connection, str(bill.invoice_number),
            )
        except Exception as find_exc:
            logger.debug(
                "[QB] find_bill pre-check failed (proceeding) "
                "vendor=%s invoice=%s: %s",
                getattr(bill, "vendor_name", None),
                bill.invoice_number, find_exc,
            )
            existing_bill = None
        if existing_bill and existing_bill.get("bill_id"):
            return {
                "status": "already_posted",
                "erp": "quickbooks",
                "bill_id": existing_bill.get("bill_id"),
                "doc_number": existing_bill.get("doc_number"),
                "amount": existing_bill.get("amount"),
                "idempotency_key": idempotency_key,
            }

    expense_account = get_account_code("quickbooks", "expenses", gl_map)

    # Build QuickBooks Bill format
    qb_bill = {
        "VendorRef": {"value": bill.vendor_id, "name": bill.vendor_name},
        "TxnDate": bill.invoice_date or datetime.now(timezone.utc).strftime("%Y-%m-%d"),
        "DueDate": bill.due_date,
        "DocNumber": bill.invoice_number,
        "PrivateNote": bill.description or f"Invoice from {bill.vendor_name}",
        "Line": [],
    }

    # Currency — post in the invoice's native currency, not the QB
    # home currency. QB requires Multicurrency to be enabled on the
    # company file for non-home-currency bills. If QB rejects because
    # Multicurrency is off, the caller will see erp_error_detail and
    # can enable it.
    bill_currency = str(getattr(bill, "currency", "") or "").strip().upper()
    if bill_currency and len(bill_currency) == 3:
        qb_bill["CurrencyRef"] = {"value": bill_currency}

    # Tax-code propagation: Solden vat_code -> QBO TaxCodeRef. Set
    # at the line level via AccountBasedExpenseLineDetail.TaxCodeRef.
    # Operators map their realm-specific TaxCode ids via gl_map (e.g.
    # gl_map["tax_code_T1"] -> "5" for the realm where TaxCode id 5 is
    # "Standard Rated Input"). Without an override we fall back to a
    # canonical default ("TAX" / "NON") used by QB Sandbox tenants.
    bill_vat_code = str(getattr(bill, "vat_code", "") or "").upper()

    def _resolve_qb_tax_code(code: str) -> Optional[str]:
        if not code:
            return None
        if gl_map and gl_map.get(f"tax_code_{code}"):
            return str(gl_map[f"tax_code_{code}"])
        return _DEFAULT_QB_TAXCODE_MAP.get(code)

    # Add line items or create single expense line.
    # money_to_float quantizes to 2dp exactly before serialisation so
    # the number we ship to QB matches what we stored internally.
    if bill.line_items:
        for i, item in enumerate(bill.line_items):
            line: Dict[str, Any] = {
                "Id": str(i + 1),
                "DetailType": "AccountBasedExpenseLineDetail",
                "Amount": money_to_float(item.get("amount", 0)),
                "Description": item.get("description", ""),
                "AccountBasedExpenseLineDetail": {
                    "AccountRef": {"value": item.get("account_id", expense_account)},
                }
            }
            line_vat = str(item.get("vat_code") or bill_vat_code or "").upper()
            tax_code = _resolve_qb_tax_code(line_vat)
            if tax_code:
                line["AccountBasedExpenseLineDetail"]["TaxCodeRef"] = {
                    "value": tax_code,
                }
            qb_bill["Line"].append(line)
    else:
        # Single line item for full amount
        single = {
            "Id": "1",
            "DetailType": "AccountBasedExpenseLineDetail",
            "Amount": money_to_float(bill.amount),
            "Description": bill.description or f"Invoice {bill.invoice_number}",
            "AccountBasedExpenseLineDetail": {
                "AccountRef": {"value": expense_account},
            }
        }
        tax_code = _resolve_qb_tax_code(bill_vat_code)
        if tax_code:
            single["AccountBasedExpenseLineDetail"]["TaxCodeRef"] = {
                "value": tax_code,
            }
        qb_bill["Line"].append(single)

    # Add tax line if tax_amount is provided
    if getattr(bill, "tax_amount", None) and bill.tax_amount > 0:
        qb_bill["TxnTaxDetail"] = {
            "TotalTax": money_to_float(bill.tax_amount),
        }

    # Apply discount if provided
    if getattr(bill, "discount_amount", None) and bill.discount_amount > 0:
        qb_bill["Line"].append({
            "Id": str(len(qb_bill["Line"]) + 1),
            "DetailType": "DiscountLineDetail",
            "Amount": money_to_float(bill.discount_amount),
            "DiscountLineDetail": {
                "PercentBased": False,
            },
        })

    # Set payment terms memo
    if getattr(bill, "payment_terms", None):
        existing_note = qb_bill.get("PrivateNote", "")
        qb_bill["PrivateNote"] = f"{existing_note} | Terms: {bill.payment_terms}".strip(" |")

    # Module 5 Pass C — stamp customer-configured custom fields onto
    # the Bill via QBO's CustomField array. QBO custom fields are
    # identified by DefinitionId (numeric) — the operator pastes the
    # DefinitionId into the field-mapping form. The resolved
    # ``custom_fields`` dict already maps DefinitionId → value.
    if custom_fields:
        cf_array = []
        for definition_id, value in custom_fields.items():
            if definition_id and value is not None:
                cf_array.append({
                    "DefinitionId": str(definition_id),
                    "Type": "StringType",
                    "StringValue": str(value)[:100],  # QBO caps custom-field strings at 100 chars
                })
        if cf_array:
            qb_bill["CustomField"] = cf_array

    # Module 5 Pass C — Class + Department/Location renames per line.
    # The catalog defaults map "class_field" → "Class" and
    # "department_field" → "Location". QBO accepts these inside the
    # AccountBasedExpenseLineDetail block as ClassRef / DepartmentRef.
    # When the customer overrides the field name we still write to the
    # standard QBO key — QBO does not allow renaming these — but we
    # surface the *value* via the catalog (so the operator's UI shows
    # which dimension is wired). i.e. the QBO catalog entries serve
    # as defaults for now; full rename would require a QBO API that
    # doesn't yet exist. Behaviour is documented in the catalog
    # description so this is not a silent contract violation.
    field_mappings_local = field_mappings or {}
    if field_mappings_local:
        # Allow the operator to opt out of stamping by leaving the
        # field blank (no override = use default QBO name; explicit
        # blank = skip entirely). This keeps existing tenants whose
        # QB company doesn't use class/location working untouched.
        pass

    url = f"https://quickbooks.api.intuit.com/v3/company/{connection.realm_id}/bill"
    if idempotency_key:
        # Intuit's requestid is capped at 50 chars; trim conservatively.
        url = f"{url}?requestid={str(idempotency_key)[:50]}"

    try:
        client = get_http_client()
        response = await client.post(
            url,
            json=qb_bill,
            headers={
                "Authorization": f"Bearer {connection.access_token}",
                "Content-Type": "application/json",
                "Accept": "application/json",
            },
            timeout=30,
        )

        if response.status_code == 401:
            return {"status": "error", "erp": "quickbooks", "reason": "Token expired", "needs_reauth": True}

        response.raise_for_status()
        result = response.json()

        bill_data = result.get("Bill", {})
        bill_id = bill_data.get("Id")
        # Wave 1 / A2 — journal entry traceability. QBO's data model
        # treats a Bill as the journal-creating transaction itself —
        # there is no separate "JournalEntry" record for a Bill the
        # way SAP B1 does. Auditor queries against QBO's JournalReport
        # use the Bill Id as the transaction identifier (Tx + TxType=
        # 'Bill'). For uniformity across ERPs we expose the bill id
        # ALSO under ``erp_journal_entry_id`` so callers can persist
        # one column regardless of ERP and the audit chain stays
        # explicit.
        logger.info("Posted Bill to QuickBooks: %s", bill_id)
        return {
            "status": "success",
            "erp": "quickbooks",
            "bill_id": bill_id,
            "doc_number": bill_data.get("DocNumber"),
            "sync_token": bill_data.get("SyncToken"),
            "erp_journal_entry_id": (str(bill_id) if bill_id is not None else None),
        }

    except httpx.HTTPStatusError as e:
        status_code = e.response.status_code
        # Parse QuickBooks error response for actionable details
        erp_error_detail = ""
        erp_error_code = ""
        try:
            error_body = e.response.json()
            fault = error_body.get("Fault", {})
            errors = fault.get("Error") or []
            if errors:
                erp_error_detail = errors[0].get("Detail") or errors[0].get("Message") or ""
                erp_error_code = errors[0].get("code") or ""
        except Exception:
            # Pre-fix this fell back to ``e.response.text[:200]``,
            # which under non-JSON paths (HTML 502s, gateway errors,
            # auth-redirect bodies) can include partial bearer
            # tokens echoed in headers, internal hostnames, or PII
            # from the bill payload — and the value lands in
            # ``last_error`` on AP items + the admin console.
            # Memory rule: no raw response bodies in returned
            # values. Log the truncated text for debug, return an
            # opaque placeholder.
            try:
                _full_text = e.response.text if hasattr(e.response, "text") else ""
            except Exception:
                _full_text = ""
            if _full_text:
                logger.debug(
                    "[QB] non-json error response (truncated): %s",
                    _full_text[:200],
                )
            erp_error_detail = f"http_{status_code}_non_json_response"

        reason = f"http_{status_code}"
        if status_code == 404:
            reason = "erp_realm_id_invalid"
        elif "Duplicate" in erp_error_detail:
            reason = "erp_duplicate_bill"
        elif "Account" in erp_error_detail and "not found" in erp_error_detail.lower():
            reason = "erp_gl_account_invalid"
        elif "Vendor" in erp_error_detail and "not found" in erp_error_detail.lower():
            reason = "erp_vendor_not_found"

        logger.error(
            "QuickBooks Bill API error: status=%d reason=%s detail=%s",
            status_code, reason, erp_error_detail[:200],
        )
        return {
            "status": "error",
            "erp": "quickbooks",
            "reason": reason,
            "erp_error_detail": erp_error_detail,
            "erp_error_code": erp_error_code,
            "needs_reauth": status_code == 401,
        }
    except Exception as e:
        # Don't return ``str(e)`` — exception messages can include
        # full URLs with query strings, SSL handshake info, or the
        # raw response body on httpx.HTTPStatusError. Log it for
        # debug, return only the exception type name.
        logger.error("QuickBooks Bill error: %s: %s", type(e).__name__, e)
        return {
            "status": "error", "erp": "quickbooks",
            "reason": "bill_posting_failed",
            "erp_error_detail": type(e).__name__,
        }


# ==================== Bill Lookup ====================

async def get_bill_quickbooks(
    connection,
    bill_id: str,
) -> Dict[str, Any]:
    """Fetch a QuickBooks bill with enough context for follow-on actions."""
    if not connection.access_token or not connection.realm_id:
        return {"status": "error", "erp": "quickbooks", "reason": "QuickBooks not properly configured"}

    safe_id = _sanitize_quickbooks_like_operand(bill_id)
    if not safe_id:
        return {"status": "error", "erp": "quickbooks", "reason": "invalid_bill_reference"}
    literal = _escape_query_literal(safe_id)
    query = (
        "SELECT Id, DocNumber, TotalAmt, Balance, VendorRef, APAccountRef "
        f"FROM Bill WHERE Id = '{literal}'"
    )
    url = f"https://quickbooks.api.intuit.com/v3/company/{connection.realm_id}/query"
    try:
        client = get_http_client()
        response = await client.get(
            url,
            params={"query": query},
            headers={"Authorization": f"Bearer {connection.access_token}"},
            timeout=30,
        )
        if response.status_code == 401:
            return {"status": "error", "erp": "quickbooks", "reason": "Token expired", "needs_reauth": True}

        response.raise_for_status()
        bills = response.json().get("QueryResponse", {}).get("Bill", [])
        if not bills:
            return {
                "status": "error",
                "erp": "quickbooks",
                "reason": "bill_not_found",
                "target_erp_reference": bill_id,
            }
        bill = bills[0]
        vendor_ref = bill.get("VendorRef") if isinstance(bill.get("VendorRef"), dict) else {}
        ap_account_ref = bill.get("APAccountRef") if isinstance(bill.get("APAccountRef"), dict) else {}
        return {
            "status": "success",
            "erp": "quickbooks",
            "bill_id": bill.get("Id"),
            "doc_number": bill.get("DocNumber"),
            "total_amt": bill.get("TotalAmt"),
            "balance": bill.get("Balance"),
            "vendor_id": vendor_ref.get("value"),
            "vendor_name": vendor_ref.get("name"),
            "ap_account_id": ap_account_ref.get("value"),
        }
    except httpx.HTTPStatusError as e:
        status_code = e.response.status_code
        logger.error("QuickBooks bill GET HTTP error: status=%d", status_code)
        reason = f"http_{status_code}"
        try:
            payload = e.response.json()
        except Exception:
            payload = None
        fault = _extract_quickbooks_fault_message(payload)
        if fault:
            reason = fault
        return {
            "status": "error",
            "erp": "quickbooks",
            "reason": reason,
            "needs_reauth": status_code == 401,
        }
    except Exception as e:
        logger.error("QuickBooks bill GET error: %s", type(e).__name__)
        return {"status": "error", "erp": "quickbooks", "reason": "bill_lookup_failed"}


# ==================== Bill Reversal ====================

async def _fetch_quickbooks_bill_with_sync_token(
    connection,
    bill_id: str,
) -> Dict[str, Any]:
    """Fetch a Bill via the REST-GET endpoint, which returns SyncToken.

    The SELECT-based ``get_bill_quickbooks`` query does not return SyncToken
    (SuiteQL doesn't expose it), so we use the REST retrieval endpoint which
    is the only way to get the current SyncToken required for delete
    operations under QuickBooks' optimistic-locking contract.

    Returns the raw Bill dict on success, or an error shape on failure.
    """
    if not connection.access_token or not connection.realm_id:
        return {
            "status": "error",
            "erp": "quickbooks",
            "reason": "QuickBooks not properly configured",
        }
    if not bill_id:
        return {"status": "error", "erp": "quickbooks", "reason": "invalid_bill_reference"}

    url = (
        f"https://quickbooks.api.intuit.com/v3/company/"
        f"{connection.realm_id}/bill/{bill_id}"
    )
    try:
        client = get_http_client()
        response = await client.get(
            url,
            headers={
                "Authorization": f"Bearer {connection.access_token}",
                "Accept": "application/json",
            },
            params={"minorversion": "73"},
            timeout=30,
        )
        if response.status_code == 401:
            return {
                "status": "error",
                "erp": "quickbooks",
                "reason": "Token expired",
                "needs_reauth": True,
            }
        if response.status_code == 404:
            return {
                "status": "error",
                "erp": "quickbooks",
                "reason": "bill_not_found",
            }
        response.raise_for_status()
        body = response.json() or {}
        bill = body.get("Bill") if isinstance(body, dict) else None
        if not isinstance(bill, dict) or not bill.get("Id"):
            return {
                "status": "error",
                "erp": "quickbooks",
                "reason": "bill_not_found",
            }
        return {
            "status": "success",
            "erp": "quickbooks",
            "bill_id": bill.get("Id"),
            "sync_token": bill.get("SyncToken"),
            "doc_number": bill.get("DocNumber"),
            "status_raw": bill.get("status"),
            "raw": bill,
        }
    except httpx.HTTPStatusError as e:
        status_code = e.response.status_code
        return {
            "status": "error",
            "erp": "quickbooks",
            "reason": f"http_{status_code}",
            "needs_reauth": status_code == 401,
        }
    except Exception as exc:
        logger.error(
            "QuickBooks bill REST GET error: %s: %s", type(exc).__name__, exc
        )
        return {
            "status": "error",
            "erp": "quickbooks",
            "reason": "bill_lookup_failed",
        }


async def reverse_bill_from_quickbooks(
    connection,
    erp_reference: str,
    *,
    reason: str,
    sync_token: Optional[str] = None,
) -> Dict[str, Any]:
    """Reverse a posted QuickBooks bill by soft-delete.

    QuickBooks Online does NOT support ``void`` for Bills — voidable
    entities are Invoices, Sales Receipts, Credit Memos, and Bill
    Payments. For Bills the only supported reversal is
    ``?operation=delete``, which soft-deletes the record (it stays in
    the audit trail but no longer affects the books).

    QuickBooks uses optimistic locking via ``SyncToken``. If the caller
    passes a stale or missing ``sync_token`` we fetch the current one
    via the REST-GET endpoint and retry once transparently.

    Returns the uniform reversal result shape. On success, the
    ``reversal_method`` is ``"delete"`` and ``reversal_ref`` is the
    original bill Id (QBO does not create a separate reversal doc).
    """
    if not connection.access_token or not connection.realm_id:
        return {
            "status": "error",
            "erp": "quickbooks",
            "reason": "QuickBooks not properly configured",
        }
    if not erp_reference:
        return {
            "status": "error",
            "erp": "quickbooks",
            "reason": "missing_erp_reference",
        }

    # Resolve a fresh SyncToken if none was supplied by the caller.
    effective_sync_token = sync_token
    if not effective_sync_token:
        fresh = await _fetch_quickbooks_bill_with_sync_token(connection, erp_reference)
        if fresh.get("status") != "success":
            # If the bill no longer exists, treat as already reversed.
            if fresh.get("reason") == "bill_not_found":
                return {
                    "status": "already_reversed",
                    "erp": "quickbooks",
                    "reference_id": erp_reference,
                    "reversal_method": "delete",
                    "reversal_ref": erp_reference,
                    "reason": "bill_not_found_in_erp",
                }
            return fresh
        effective_sync_token = fresh.get("sync_token")

    url = f"https://quickbooks.api.intuit.com/v3/company/{connection.realm_id}/bill"
    payload = {"Id": erp_reference, "SyncToken": str(effective_sync_token or "0")}

    async def _attempt_delete(token_to_use: str) -> httpx.Response:
        client = get_http_client()
        return await client.post(
            url,
            json={**payload, "SyncToken": token_to_use},
            headers={
                "Authorization": f"Bearer {connection.access_token}",
                "Content-Type": "application/json",
                "Accept": "application/json",
            },
            params={"operation": "delete"},
            timeout=30,
        )

    try:
        response = await _attempt_delete(str(effective_sync_token or "0"))

        if response.status_code == 401:
            return {
                "status": "error",
                "erp": "quickbooks",
                "reason": "Token expired",
                "needs_reauth": True,
            }

        # QBO returns 400 with a "stale object" fault when SyncToken is
        # out of date. Refetch once and retry transparently.
        if response.status_code == 400:
            try:
                fault = response.json()
            except Exception:
                fault = None
            fault_text = _extract_quickbooks_fault_message(fault) or ""
            if "stale" in fault_text.lower() or "version" in fault_text.lower():
                logger.info(
                    "QuickBooks SyncToken stale for bill %s — refetching and retrying once",
                    erp_reference,
                )
                fresh = await _fetch_quickbooks_bill_with_sync_token(
                    connection, erp_reference
                )
                if fresh.get("status") != "success":
                    if fresh.get("reason") == "bill_not_found":
                        return {
                            "status": "already_reversed",
                            "erp": "quickbooks",
                            "reference_id": erp_reference,
                            "reversal_method": "delete",
                            "reversal_ref": erp_reference,
                            "reason": "bill_not_found_in_erp",
                        }
                    return fresh
                response = await _attempt_delete(str(fresh.get("sync_token") or "0"))

        # After the possible retry, handle the now-current response.
        if response.status_code == 404:
            return {
                "status": "already_reversed",
                "erp": "quickbooks",
                "reference_id": erp_reference,
                "reversal_method": "delete",
                "reversal_ref": erp_reference,
                "reason": "bill_not_found_in_erp",
            }

        response.raise_for_status()
        result = response.json() or {}
        bill_block = result.get("Bill") if isinstance(result, dict) else None
        status_raw = ""
        if isinstance(bill_block, dict):
            status_raw = str(bill_block.get("status") or "")

        logger.info(
            "Reversed QuickBooks Bill %s (reason=%s)", erp_reference, reason
        )
        return {
            "status": "success",
            "erp": "quickbooks",
            "reference_id": erp_reference,
            "reversal_method": "delete",
            "reversal_ref": erp_reference,
            "erp_status": status_raw or "Deleted",
        }

    except httpx.HTTPStatusError as e:
        status_code = e.response.status_code
        erp_error_detail = ""
        try:
            fault_payload = e.response.json()
            erp_error_detail = _extract_quickbooks_fault_message(fault_payload) or ""
        except Exception:
            erp_error_detail = (
                e.response.text[:200] if hasattr(e.response, "text") else ""
            )

        detail_lower = erp_error_detail.lower()
        reason_code = f"http_{status_code}"
        if status_code == 401:
            reason_code = "authentication_failed"
        elif "payment" in detail_lower and (
            "applied" in detail_lower or "linked" in detail_lower
        ):
            reason_code = "payment_already_applied"
        elif "not found" in detail_lower or status_code == 404:
            return {
                "status": "already_reversed",
                "erp": "quickbooks",
                "reference_id": erp_reference,
                "reversal_method": "delete",
                "reversal_ref": erp_reference,
                "reason": "bill_not_found_in_erp",
            }

        logger.error(
            "QuickBooks Bill reverse HTTP error: status=%d reason=%s detail=%s",
            status_code, reason_code, erp_error_detail[:200],
        )
        return {
            "status": "error",
            "erp": "quickbooks",
            "reference_id": erp_reference,
            "reversal_method": "delete",
            "reason": reason_code,
            "erp_error_detail": erp_error_detail,
            "needs_reauth": status_code == 401,
        }
    except Exception as exc:
        logger.error(
            "QuickBooks Bill reverse error: %s: %s", type(exc).__name__, exc
        )
        return {
            "status": "error",
            "erp": "quickbooks",
            "reference_id": erp_reference,
            "reversal_method": "delete",
            "reason": "bill_reversal_failed",
            "erp_error_detail": type(exc).__name__,
        }


