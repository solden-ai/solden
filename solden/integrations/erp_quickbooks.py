async def get_payment_status_quickbooks(
    connection,
    bill_id: str,
) -> Dict[str, Any]:
    """Read payment status for a QuickBooks bill. GET only — never executes payments.

    Checks the Bill's Balance field: Balance == 0 means fully paid.
    If Balance < TotalAmt but > 0, it's a partial payment.
    """
    if not connection.access_token or not connection.realm_id:
        return {"paid": False, "error": "QuickBooks not properly configured"}

    safe_id = _sanitize_quickbooks_like_operand(bill_id)
    if not safe_id:
        return {"paid": False, "error": "invalid_bill_reference"}
    literal = _escape_query_literal(safe_id)
    query = (
        "SELECT Id, DocNumber, TotalAmt, Balance, VendorRef "
        f"FROM Bill WHERE Id = '{literal}'"
    )
    url = f"https://quickbooks.api.intuit.com/v3/company/{connection.realm_id}/query"
    try:
        client = get_http_client()
        response = await client.get(
            url,
            params={"query": query},
            headers={"Authorization": f"Bearer {connection.access_token}"},
            timeout=30,
        )
        if response.status_code == 401:
            return {"paid": False, "error": "token_expired", "needs_reauth": True}

        response.raise_for_status()
        bills = response.json().get("QueryResponse", {}).get("Bill", [])
        if not bills:
            return {"paid": False, "reason": "not_found"}

        bill = bills[0]
        total_amt = float(bill.get("TotalAmt") or 0)
        balance = float(bill.get("Balance") or 0)
        bill_id_val = bill.get("Id") or ""

        if balance <= 0 and total_amt > 0:
            # Fully paid — derive closure_method from the bill's
            # LinkedTxn rows (BillPayment vs VendorCredit). The dedicated
            # BillPayment/VendorCredit Query API would give a richer
            # answer but isn't required for the closure-method label.
            closure_method = "payment"
            try:
                if not bill.get("LinkedTxn"):
                    closure_method = "unknown_non_payment"
                else:
                    linked = bill.get("LinkedTxn") or []
                    has_payment = any(
                        str(lt.get("TxnType") or "").lower() == "billpayment"
                        for lt in (linked if isinstance(linked, list) else [])
                    )
                    has_credit = any(
                        str(lt.get("TxnType") or "").lower() in ("vendorcredit", "creditcardcredit")
                        for lt in (linked if isinstance(linked, list) else [])
                    )
                    if has_credit and not has_payment:
                        closure_method = "credit_applied"
                    elif has_payment:
                        closure_method = "payment"
                    else:
                        closure_method = "unknown_non_payment"
            except Exception:
                pass  # Fall through with default closure_method

            result = {
                "paid": True,
                "payment_amount": round(total_amt, 2),
                "payment_date": "",
                "payment_method": "",
                "payment_reference": bill_id_val,
                "partial": False,
                "remaining_balance": 0.0,
            }
            if closure_method != "payment":
                result["closure_method"] = closure_method
            return result

        elif balance < total_amt:
            # Partial payment
            paid_amount = round(total_amt - balance, 2)
            return {
                "paid": False,
                "payment_amount": paid_amount,
                "payment_date": "",
                "payment_method": "",
                "payment_reference": bill_id_val,
                "partial": True,
                "remaining_balance": round(balance, 2),
            }
        else:
            # Check for voided payments (Balance == TotalAmt but voided
            # BillPayments exist in LinkedTxn)
            linked = bill.get("LinkedTxn") or []
            if isinstance(linked, list):
                has_voided = any(
                    str(lt.get("TxnType") or "").lower() == "billpayment"
                    for lt in linked
                )
                if has_voided and balance >= total_amt and total_amt > 0:
                    return {
                        "paid": False,
                        "payment_failed": True,
                        "reason": "payment_voided",
                    }
            return {"paid": False, "reason": "unpaid"}
    except httpx.HTTPStatusError as e:
        logger.error("QuickBooks payment status HTTP error: status=%d", e.response.status_code)
        return {"paid": False, "error": f"http_{e.response.status_code}"}
    except Exception as e:
        logger.error("QuickBooks payment status error: %s", type(e).__name__)
        return {"paid": False, "error": "payment_status_lookup_failed"}


# ==================== BillPayment Fetch (Wave 2 / C3) ====================


async def fetch_quickbooks_bill_payment(
    connection,
    bill_payment_id: str,
) -> Dict[str, Any]:
    """Fetch a BillPayment by its QuickBooks Id.

    Used by the inbound payment webhook receiver: when QB pushes a
    notification of a BillPayment.create / .update event we follow up
    with this read so we know which Bill(s) it cleared, the rail
    (Check / CreditCard), the date, and the cleared amount.

    Returns either::

        {
            "status": "success",
            "bill_payment_id": str,
            "txn_date": str | None,         # YYYY-MM-DD
            "total_amount": float,
            "currency": str | None,         # ISO 4217 from CurrencyRef
            "private_note": str | None,
            "pay_type": str | None,         # "Check" | "CreditCard" | ...
            "linked_bills": [{"bill_id": str, "amount": float}, ...],
            "voided": bool,                 # True if PrivateNote indicates void
        }

    or::

        {"status": "error", "reason": str, "needs_reauth": bool}
    """
    if not connection.access_token or not connection.realm_id:
        return {
            "status": "error",
            "reason": "QuickBooks not properly configured",
        }

    safe_id = _sanitize_quickbooks_like_operand(bill_payment_id)
    if not safe_id:
        return {"status": "error", "reason": "invalid_bill_payment_id"}

    url = (
        f"https://quickbooks.api.intuit.com/v3/company/"
        f"{connection.realm_id}/billpayment/{safe_id}"
    )
    try:
        client = get_http_client()
        response = await client.get(
            url,
            headers=_quickbooks_headers(connection),
            timeout=_ERP_TIMEOUT,
        )
        if response.status_code == 401:
            return {
                "status": "error",
                "reason": "token_expired",
                "needs_reauth": True,
            }
        if response.status_code == 404:
            return {"status": "error", "reason": "not_found"}
        response.raise_for_status()
        body = response.json() or {}
        bp = body.get("BillPayment") or {}
        if not bp:
            return {"status": "error", "reason": "no_bill_payment_in_response"}

        linked: List[Dict[str, Any]] = []
        for line in bp.get("Line") or []:
            if not isinstance(line, dict):
                continue
            line_amount = line.get("Amount")
            for lt in line.get("LinkedTxn") or []:
                if not isinstance(lt, dict):
                    continue
                if str(lt.get("TxnType") or "").lower() != "bill":
                    continue
                tid = str(lt.get("TxnId") or "").strip()
                if tid:
                    linked.append({
                        "bill_id": tid,
                        "amount": (
                            float(line_amount)
                            if line_amount is not None else None
                        ),
                    })

        currency = None
        currency_ref = bp.get("CurrencyRef")
        if isinstance(currency_ref, dict):
            currency = currency_ref.get("value") or None

        private_note = bp.get("PrivateNote")
        pay_type = str(bp.get("PayType") or "").strip() or None

        # QB doesn't expose a "Voided" flag directly on BillPayment;
        # convention is to set PrivateNote to "Voided" when voiding.
        voided = bool(
            isinstance(private_note, str)
            and "void" in private_note.lower()
        )

        return {
            "status": "success",
            "bill_payment_id": str(bp.get("Id") or safe_id),
            "txn_date": bp.get("TxnDate"),
            "total_amount": float(bp.get("TotalAmt") or 0.0),
            "currency": currency,
            "private_note": private_note,
            "pay_type": pay_type,
            "linked_bills": linked,
            "voided": voided,
        }
    except httpx.HTTPStatusError as exc:
        sc = exc.response.status_code
        return {"status": "error", "reason": f"http_{sc}"}
    except Exception as exc:
        logger.warning(
            "fetch_quickbooks_bill_payment failed: %s",
            type(exc).__name__,
        )
        return {"status": "error", "reason": "bill_payment_fetch_failed"}


