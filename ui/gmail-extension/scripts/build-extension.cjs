#!/usr/bin/env node

const fs = require('node:fs');
const path = require('node:path');
const { spawnSync } = require('node:child_process');
const {
  BUNDLE_FINGERPRINT_BANNER_PREFIX,
  computeSourceFingerprint,
} = require('./source-fingerprint.cjs');
const { verifyBundleParity } = require('./verify-bundle-parity.cjs');

const EXTENSION_ROOT = path.resolve(__dirname, '..');
const DIST_ROOT = path.join(EXTENSION_ROOT, 'dist');
const BUILD_ROOT = path.join(EXTENSION_ROOT, 'build');
const BUILD_DIST_ROOT = path.join(BUILD_ROOT, 'dist');
const FINGERPRINT_PATH = path.join(DIST_ROOT, 'inboxsdk-layer.meta.json');
const BUN_BIN = process.env.CLEARLEDGR_BUN_BIN || 'bun';
const BUILD_DEBUG = process.env.CLEARLEDGR_BUILD_DEBUG === '1';
const BUILD_DEBUG_FILE = String(process.env.CLEARLEDGR_BUILD_DEBUG_FILE || '').trim();
const UNPACKED_STATIC_FILES = ['manifest.json', 'background.js', 'queue-manager.js', 'content-script.js', 'route-capture.js'];
const UNPACKED_STATIC_DIRS = ['icons', 'clients', 'workflows', 'vendor', 'engines'];

function parseArgs(argv) {
  const args = { config: 'webpack.dev.cjs', verify: false };
  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === '--config' && argv[i + 1]) {
      args.config = argv[i + 1];
      i += 1;
      continue;
    }
    if (token === '--verify') {
      args.verify = true;
    }
  }
  return args;
}

function debug(message) {
  if (!BUILD_DEBUG) return;
  const line = `[build-extension:debug] ${message}`;
  console.log(line);
  if (BUILD_DEBUG_FILE) {
    fs.appendFileSync(BUILD_DEBUG_FILE, `${line}\n`, 'utf8');
  }
}

function writeFingerprintMetadata(payload) {
  if (!fs.existsSync(DIST_ROOT)) {
    fs.mkdirSync(DIST_ROOT, { recursive: true });
  }
  fs.writeFileSync(FINGERPRINT_PATH, `${JSON.stringify({
    generated_at: new Date().toISOString(),
    ...payload,
  }, null, 2)}\n`, 'utf8');
}

function applyBundleFingerprintBanner(fingerprint) {
  const bundlePath = path.join(DIST_ROOT, 'inboxsdk-layer.js');
  if (!fs.existsSync(bundlePath)) {
    throw new Error(`Missing built bundle for fingerprint banner injection: ${bundlePath}`);
  }
  const bannerComment = `/* ${BUNDLE_FINGERPRINT_BANNER_PREFIX}${fingerprint} */`;
  const existing = fs.readFileSync(bundlePath, 'utf8');
  const normalized = existing.replace(
    new RegExp(`^/\\*\\s*${BUNDLE_FINGERPRINT_BANNER_PREFIX}[a-f0-9]+\\s*\\*/\\n?`),
    '',
  );
  fs.writeFileSync(bundlePath, `${bannerComment}\n${normalized}`, 'utf8');
}

function cleanDist() {
  fs.rmSync(DIST_ROOT, { recursive: true, force: true });
  fs.mkdirSync(DIST_ROOT, { recursive: true });
}

function inferBuildEnvFromApiUrl(apiUrl) {
  const normalized = String(apiUrl || '').trim().toLowerCase();
  if (!normalized) return 'prod';
  if (normalized.includes('127.0.0.1') || normalized.includes('localhost')) return 'dev';
  if (normalized.includes('staging')) return 'staging';
  if (normalized.includes('railway.app')) return 'railway';
  return 'prod';
}

function readExistingBuildConfig() {
  const configPath = path.join(BUILD_ROOT, 'config.js');
  if (!fs.existsSync(configPath)) return null;
  const raw = fs.readFileSync(configPath, 'utf8');
  const apiUrl = raw.match(/API_URL:\s*'([^']+)'/)?.[1] || '';
  const env = raw.match(/ENV:\s*'([^']+)'/)?.[1] || '';
  if (!apiUrl && !env) return null;
  return { apiUrl, env };
}

function resolveBuildConfig() {
  const existing = readExistingBuildConfig();
  const apiUrl = String(
    process.env.SOLDEN_EXTENSION_API_URL
    || process.env.SOLDEN_API_URL
    || process.env.CLEARLEDGR_EXTENSION_API_URL  // legacy alias
    || process.env.CLEARLEDGR_API_URL            // legacy alias
    || process.env.API_BASE_URL
    || existing?.apiUrl
    || 'https://api.soldenai.com',
  ).trim();
  const env = String(
    process.env.SOLDEN_EXTENSION_ENV
    || process.env.CLEARLEDGR_EXTENSION_ENV  // legacy alias
    || existing?.env
    || inferBuildEnvFromApiUrl(apiUrl),
  ).trim();
  return { apiUrl, env };
}

function ensureDir(targetPath) {
  fs.mkdirSync(targetPath, { recursive: true });
}

function syncEntryToBuild(entryName) {
  const sourcePath = path.join(EXTENSION_ROOT, entryName);
  const targetPath = path.join(BUILD_ROOT, entryName);
  if (!fs.existsSync(sourcePath)) return;
  fs.rmSync(targetPath, { recursive: true, force: true });
  const stats = fs.statSync(sourcePath);
  if (stats.isDirectory()) {
    fs.cpSync(sourcePath, targetPath, { recursive: true });
    return;
  }
  ensureDir(path.dirname(targetPath));
  fs.copyFileSync(sourcePath, targetPath);
}

function writeBuildConfig() {
  const { apiUrl, env } = resolveBuildConfig();
  const manifestPath = path.join(EXTENSION_ROOT, 'manifest.json');
  const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
  const version = String(manifest?.version || '0.0.0');
  const configPath = path.join(BUILD_ROOT, 'config.js');
  fs.writeFileSync(configPath, `// Auto-generated by build-extension.cjs - DO NOT EDIT
const SOLDEN_CONFIG = {
  API_URL: '${apiUrl}',
  ENV: '${env}',
  VERSION: '${version}',
  BUILD_TIME: '${new Date().toISOString()}'
};

if (typeof window !== 'undefined') {
  window.SOLDEN_CONFIG = SOLDEN_CONFIG;
}
if (typeof self !== 'undefined') {
  self.SOLDEN_CONFIG = SOLDEN_CONFIG;
}
`, 'utf8');
  return { apiUrl, env };
}

function updateBuildManifestHostPermissions(apiUrl) {
  const manifestPath = path.join(BUILD_ROOT, 'manifest.json');
  if (!fs.existsSync(manifestPath)) return;
  let apiHostPermission = '';
  try {
    const parsed = new URL(String(apiUrl || '').trim());
    apiHostPermission = `${parsed.origin}/*`;
  } catch (_) {
    apiHostPermission = '';
  }
  if (!apiHostPermission) return;
  const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
  const hostPermissions = Array.isArray(manifest.host_permissions) ? manifest.host_permissions : [];
  manifest.host_permissions = Array.from(new Set([...hostPermissions, apiHostPermission]));
  fs.writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`, 'utf8');
}

function injectBuildConfigIntoBackground() {
  const backgroundPath = path.join(BUILD_ROOT, 'background.js');
  if (!fs.existsSync(backgroundPath)) return;
  const existing = fs.readFileSync(backgroundPath, 'utf8');
  if (/^\s*importScripts\(["']config\.js["']\);\s*/.test(existing)) return;
  fs.writeFileSync(backgroundPath, `importScripts("config.js");\n${existing}`, 'utf8');
}

function syncUnpackedBuild() {
  ensureDir(BUILD_ROOT);
  UNPACKED_STATIC_DIRS.forEach(syncEntryToBuild);
  UNPACKED_STATIC_FILES.forEach(syncEntryToBuild);
  fs.rmSync(BUILD_DIST_ROOT, { recursive: true, force: true });
  ensureDir(BUILD_DIST_ROOT);
  fs.cpSync(DIST_ROOT, BUILD_DIST_ROOT, { recursive: true });
  const { apiUrl } = writeBuildConfig();
  updateBuildManifestHostPermissions(apiUrl);
  injectBuildConfigIntoBackground();
  const now = new Date();
  fs.utimesSync(BUILD_ROOT, now, now);
}

function runBundler(configFile) {
  const sourcemap = configFile.includes('prod') ? 'external' : 'none';
  const args = [
    'build',
    './src/inboxsdk-layer.js',
    './node_modules/@inboxsdk/core/pageWorld.js',
    '--target=browser',
    '--format=iife',
    `--sourcemap=${sourcemap}`,
    '--outdir',
    'dist',
    '--entry-naming=[name].js',
  ];
  debug(`bun:${BUN_BIN} ${args.join(' ')}`);
  const result = spawnSync(BUN_BIN, args, {
    cwd: EXTENSION_ROOT,
    encoding: 'utf8',
  });
  if (result.error) {
    if (result.error.code === 'ENOENT') {
      throw new Error(`Bun is required for extension builds. Install bun or set CLEARLEDGR_BUN_BIN to a valid Bun binary.`);
    }
    throw result.error;
  }
  if (result.status !== 0) {
    const stderr = String(result.stderr || '').trim();
    const stdout = String(result.stdout || '').trim();
    throw new Error(stderr || stdout || `bun build failed with exit code ${result.status}`);
  }
  return [result.stdout, result.stderr].filter(Boolean).join('');
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  debug(`args:${JSON.stringify(args)}`);
  console.log('[build-extension] computing source fingerprint...');
  const fingerprintStartedAt = Date.now();
  debug('fingerprint:start');
  const fingerprintPayload = computeSourceFingerprint(EXTENSION_ROOT);
  debug('fingerprint:done');
  debug(`fingerprint:${fingerprintPayload.fingerprint}`);
  console.log(`[build-extension] fingerprint ready in ${Date.now() - fingerprintStartedAt}ms`);
  cleanDist();
  debug('dist cleaned');
  console.log('[build-extension] bundling InboxSDK layer...');
  const summary = runBundler(args.config);
  debug('bun build complete');
  debug('injecting fingerprint banner');
  applyBundleFingerprintBanner(fingerprintPayload.fingerprint);
  debug('writing fingerprint metadata');
  writeFingerprintMetadata(fingerprintPayload);
  debug('syncing unpacked build');
  syncUnpackedBuild();
  if (summary) {
    process.stdout.write(summary.endsWith('\n') ? summary : `${summary}\n`);
  }
  console.log(`[build-extension] wrote ${path.relative(EXTENSION_ROOT, FINGERPRINT_PATH)}`);

  if (args.verify) {
    debug('starting parity verification');
    console.log('[build-extension] verifying bundle parity...');
    verifyBundleParity();
  }

  await new Promise((resolve) => process.stdout.write('', resolve));
  await new Promise((resolve) => process.stderr.write('', resolve));
  process.exit(0);
}

main().catch((error) => {
  console.error('[build-extension] failed:', error?.message || error);
  process.exit(1);
});
